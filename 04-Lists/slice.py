# List - mutable
piano_key_list = ["a", "b", "c", "d", "e", "f", "g"]

print(piano_key_list[2:5])

print(piano_key_list[2:5:2])

print(piano_key_list[::2])

print(piano_key_list[::-1])


# Tuple - immutable
piano_key_tuple = ["do", "re", "mi", "fa", "so", "la", "ti"]

print(piano_key_tuple[2:5])

print(piano_key_tuple[2:5:2])

print(piano_key_tuple[::2])

print(piano_key_tuple[::-1])
